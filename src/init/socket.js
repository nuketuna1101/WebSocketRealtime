import { Server as SocketIO } from 'socket.io';
import registerHandler from '../handlers/register.handler.js';

// 소켓 초기화, 이벤트 핸들러 등록
const initSocket = (server) => {
    const io = new SocketIO();
    io.attach(server);
    registerHandler(io);
};

export default initSocket;